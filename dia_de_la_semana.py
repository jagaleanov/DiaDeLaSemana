# Escribir un programa que dado un número del 1 al 7 
# escriba el correspondiente nombre del día de la semana

numero = int(input("Ingrese un número entre 1 y 7: "))

if numero == 1:
  print("Lunes")
elif numero == 2:
  print("Martes")
elif numero == 3:
  print("Miercoles")
elif numero == 4:
  print("Jueves")
elif numero == 5:
  print("Viernes")
elif numero == 6:
  print("Sabado")
elif numero == 7:
  print("Domingo")